const url = 'http://api.geonames.org/searchJSON?username=ksuhiyp&maxRows=1000&country=';
let writer = fs.createWriteStream('./cities.json');

async function wait(ms) {
	return new Promise(resolve => {
	  setTimeout(resolve, ms);
	});
  }

const fetchPublicReposCity = async (isoCode, index) => {
	const d = new Date();
	const start = d.getMilliseconds();

	const response = await fetch(`${url}${isoCode}`);
	const json = await response.json();
	
	const end = d.getMilliseconds();
	console.log(index, ' - Extracted: ', isoCode, ' in: ', end, ' miliseconds');
	return json;
  }

const fetchAllIsoCodes = async () => {
	let index = 0;
	const promises = isoCodes.map(async (isoCode) => {
	  const response = await fetchPublicReposCity(isoCode, index);
	  index++;
	  await wait(1000);
	  return response;
	});
	return Promise.all(promises);
}

const main = async () => {
	const d = new Date();
	const start = d.getMilliseconds();

	const response = await fetchAllIsoCodes();
	writer.write(JSON.stringify(response)); // (response, null, 4);

	const end = d.getMilliseconds();
	console.log('Sucessfully written...', end - start);
}

